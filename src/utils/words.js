// ─── GET WORDS BY CATEGORY ────────────────────────────────────
export const getWordsByCategory = (catId) => {
  const filtered = WORDS.filter(w => w.cat === catId && w.word.length >= 3 && w.word.length <= 8);
  // Shuffle
  return filtered.sort(() => Math.random() - 0.5);
};

// ─── GET LETTERS FOR LEVEL ────────────────────────────────────
// Returns 6-8 unique letters that can form the target words
export const getLettersForWords = (words) => {
  if (!words.length) return [];
  // Take top 5 words, collect their letters
  const pool = new Set();
  const target = words.slice(0, Math.min(6, words.length));
  target.forEach(w => w.word.split("").forEach(l => pool.add(l)));
  let letters = [...pool];
  // Keep 6–9 letters max (add random filler if needed)
  const UZBEK_COMMON = "AEIOULMNRTSKBDGPQHVF".split("");
  while (letters.length < 6) {
    const r = UZBEK_COMMON[Math.floor(Math.random()*UZBEK_COMMON.length)];
    if (!letters.includes(r)) letters.push(r);
  }
  if (letters.length > 9) letters = letters.slice(0, 9);
  return letters.sort(() => Math.random() - 0.5);
};
